// File: D01_StarTriangle.java
// Purpose: Print a simple star triangle pattern

public class D01_StarTriangle {
    public static void main(String[] args) {
        int rows = 5;
        for (int i = 1; i <= rows; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}