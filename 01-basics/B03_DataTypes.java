// File: 03_DataTypes.java
// Class Name: B03_DataTypes
// Purpose: Shows different primitive and reference data types in Java

public class B03_DataTypes {

    public static void main(String[] args) {

        // Primitive types
        int a = 10;
        double b = 20.5;
        char c = 'A';
        boolean flag = true;

        // Print each value
        System.out.println("Int: " + a + ", Double: " + b + ", Char: " + c + ", Boolean: " + flag);

        // Reference type (example)
        String text = "Java Learning";
        System.out.println("String: " + text);
    }
}