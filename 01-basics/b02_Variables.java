// File: 02_Variables.java
// Class Name: B02_Variables
// Purpose: Demonstrates declaration and usage of variables

public class B02_Variables {

    public static void main(String[] args) {

        // Declare variables of different types
        int age = 25;               // Integer
        double salary = 50000.5;    // Decimal number
        String name = "Aman";       // Text
        boolean isStudent = true;   // True/False value

        // Print variables to the console
        System.out.println("Name: " + name + ", Age: " + age + ", Salary: " + salary + ", Student: " + isStudent);
    }
}